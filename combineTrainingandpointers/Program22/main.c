//C Program to find biggest of four no by using ternary numbers
#include <stdio.h>

int main()
{
    int num1,num2,num3,num4,max;
    printf("Enter the four integer number\n");
    scanf("%d\n %d\n %d\n %d",&num1,&num2,&num3,&num4);
    printf("The max number is=%d\n",max);

    return 0;
}
